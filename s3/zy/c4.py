# 回文数
n = int(input("请输入一个5位整数："))  # 输入一个5位整数
w = int(n / 10000)  # 取出万位数
q = int(n % 10000 / 1000)  # 取出千位数
b = int(n % 1000 / 100)  # 取出百位数
s = int(n % 100 / 10)  # 取出十位数
g = int(n % 10)  # 取出个位数
h = g * 10000 + s * 1000 + b * 100 + q * 10 + w  # 将各个位数相加
if n == h:  # 判断是否是回文数
    print("是回文数")  # 输出是回文数
else:  # 否则
    print("不是回文数")  # 输出不是回文数
