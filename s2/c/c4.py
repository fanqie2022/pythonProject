import turtle as t #导入turtle库，并取别名为s
t.pu() #画笔抬起
t.goto(0,100) #移动画笔到0，100
t.pd() #画笔落下
for i in range(2): #循环两次
    t.fd(100) #画100dpi的直线
    t.left(90+30) #左转120度
t.fd(100) #画100dpi的直线
